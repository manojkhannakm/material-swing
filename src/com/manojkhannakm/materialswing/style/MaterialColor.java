    public MaterialColor withAlpha(int alpha) {
        if (alpha < 0) {
            alpha = 0;
        } else if (alpha > 255) {
            alpha = 255;
        }

        return new MaterialColor(alpha, getRed(), getGreen(), getBlue());
    }

    public MaterialColor withAlpha(float alpha) {
        if (alpha < 0.0f) {
            alpha = 0.0f;
        } else if (alpha > 1.0f) {
            alpha = 1.0f;
        }

        return new MaterialColor(alpha, getRed() / 255.0f, getGreen() / 255.0f, getBlue() / 255.0f);
    }


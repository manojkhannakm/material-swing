    public MaterialColor withBrightness(float brightness) {
        if (brightness < 0.0f) {
            brightness = 0.0f;
        } else if (brightness > 1.0f) {
            brightness = 1.0f;
        }

        return new MaterialColor(getAlpha(), Math.round(getRed() + (255.0f - getRed()) * brightness),
                Math.round(getGreen() + (255.0f - getGreen()) * brightness),
                Math.round(getBlue() + (255.0f - getBlue()) * brightness));
    }

